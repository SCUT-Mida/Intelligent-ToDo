export const REPO_NAV_GUIDE = `# 仓库导航使用指南

扫描本地 Git 仓库，一键在终端打开并执行预设命令。

## 快速上手

1. **配置扫描根目录**：设置 → 仓库导航 → 扫描配置，添加你的开发目录（如 \`D:\\Coding\`）
2. **刷新扫描**：点击「刷新」按钮，等待扫描完成
3. **打开仓库**：点击任意仓库卡片的「打开」按钮，自动在终端中执行预设命令

## 命令模板

命令模板由多个独立命令按顺序组合：

- 设置 → 仓库导航 → 命令配置：定义可复用的命令（如 \`git pull\`、\`opencode\`）
- 设置 → 仓库导航 → 命令模板：把命令组合成模板（如「默认」= git pull + opencode）
- 仓库卡片顶部的下拉选择模板

## 收藏和标签

- **收藏**：点击仓库卡片右上角的 ☆ 可收藏，顶部「★ 收藏」标签可快速过滤
- **自定义标签**：点击「+ 标签」添加你的标签，标签可搜索
- **打开次数**：每次打开自动计数，常用的仓库自动排在前面

## AI 记忆

点击「生成 AI 记忆」，AI 会读取每个仓库的 README.md 生成描述和标签：

- 生成的标签和描述会出现在搜索匹配范围内
- 搜索是本地匹配（毫秒级），不走 AI 调用
- 需要 AI 模型已配置

## 工具路径（高级）

如果 \`git\` 或 \`wt.exe\` 不在 PATH 中，可在设置中手动指定路径：

- 每个工具支持「浏览…」文件选择和「检测」一键验证
- 检测成功自动回填绝对路径

## 终端启动

点击「打开」后：

- 优先使用 **Windows Terminal**（wt.exe）新标签页
- wt.exe 不可用时回退到 **PowerShell**
- 终端窗口会自动 cd 到仓库目录，然后依次执行模板中的命令`
